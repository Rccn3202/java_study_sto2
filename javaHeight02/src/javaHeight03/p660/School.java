package javaHeight03.p660;

public class School {

	private String name;
	private Data2 data1;
	public School(String name, Data2 data1) {
		super();
		this.name = name;
		this.data1 = data1;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Data2 getData1() {
		return data1;
	}
	public void setData1(Data2 data1) {
		this.data1 = data1;
	}
}
