package javaHeight03.p675;

public class Bus {
	private String de;
	private int stop;
	public String getDe() {
		return de;
	}
	public void setDe(String de) {
		this.de = de;
	}
	public int getStop() {
		return stop;
	}
	public void setStop(int stop) {
		this.stop = stop;
	}
	public Bus(String de, int stop) {
		super();
		this.de = de;
		this.stop = stop;
	}
	
}
